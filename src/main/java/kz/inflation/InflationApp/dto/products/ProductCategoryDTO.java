package kz.inflation.InflationApp.dto.products;

public class ProductCategoryDTO {
    private Long id;
    private String name;

    public ProductCategoryDTO(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
